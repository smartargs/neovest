#!/usr/bin/env node
/**
 * Bundles `VestingVault.nef` and `VestingVault.manifest.json` into a generated
 * TS module so the UI can deploy a fresh vault via the connected wallet
 * without requiring the user to have the toolchain locally.
 *
 * Wired into `predev` and `prebuild` alongside `sync-checksum`.
 *
 * If the compiled artifacts are missing, this script does NOT overwrite an
 * existing committed `nef-artifacts.ts` — it leaves the last-committed bundle
 * in place. This is what lets the UI build in a CI environment that has no
 * Java/Gradle toolchain (e.g. Cloudflare Pages): the committed artifacts are
 * the source of truth there. Empty placeholders are only ever written on a
 * first-time scaffold where no committed file exists at all.
 */

import { readFileSync, writeFileSync, existsSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const nefPath = resolve(here, '..', '..', 'contract', 'build', 'neow3j', 'VestingVault.nef');
const manifestPath = resolve(here, '..', '..', 'contract', 'build', 'neow3j', 'VestingVault.manifest.json');
const outPath = resolve(here, '..', 'src', 'lib', 'nef-artifacts.ts');

const compiled = existsSync(nefPath) && existsSync(manifestPath);

if (!compiled) {
  console.warn('[sync-artifacts] Compiled artifacts missing (no contract toolchain in this environment?).');
  if (existsSync(outPath)) {
    // Keep whatever is committed — do not clobber a valid bundle with empties.
    console.warn(`[sync-artifacts] Keeping existing committed ${outPath}.`);
    process.exit(0);
  }
  console.warn('[sync-artifacts] No committed artifacts either — writing empty placeholder.');
  console.warn('[sync-artifacts] Run `./gradlew :contract:neow3jCompile` then re-run sync-artifacts.');
}

const nefBase64 = compiled ? readFileSync(nefPath).toString('base64') : '';
const manifestJson = compiled ? readFileSync(manifestPath, 'utf8') : '';
const note = compiled
  ? `Bundled from ${nefPath} and ${manifestPath}.`
  : 'NEF/manifest not yet compiled — UI deploy disabled until artifacts exist.';

const generated = `// AUTO-GENERATED by scripts/sync-artifacts.mjs — do not edit by hand.
// ${note}

/** VestingVault.nef as base64. Decode at call time to a {@link Uint8Array}. */
export const NEF_BASE64: string = ${JSON.stringify(nefBase64)};

/** VestingVault.manifest.json as a UTF-8 JSON string. */
export const MANIFEST_JSON: string = ${JSON.stringify(manifestJson)};

/** True iff the artifacts were successfully bundled at build time. */
export const ARTIFACTS_AVAILABLE: boolean = ${nefBase64 !== '' && manifestJson !== ''};
`;

mkdirSync(dirname(outPath), { recursive: true });

const existing = existsSync(outPath) ? readFileSync(outPath, 'utf8') : '';
if (existing === generated) {
  console.log(`[sync-artifacts] up-to-date (nef ${nefBase64.length}b, manifest ${manifestJson.length}b)`);
} else {
  writeFileSync(outPath, generated, 'utf8');
  console.log(`[sync-artifacts] wrote ${outPath} (nef ${nefBase64.length}b, manifest ${manifestJson.length}b)`);
}
