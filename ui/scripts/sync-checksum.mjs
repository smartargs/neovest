#!/usr/bin/env node
/**
 * Reads `contract/build/neow3j/VestingVault.nef` and writes two commitments
 * into `src/lib/nef-checksum.ts`, keeping the UI's "Verified" badge in
 * lockstep with whatever was last compiled:
 *
 *   - EXPECTED_NEF_SCRIPT_SHA256 — SHA-256 of the NEF *script* bytes (the
 *     actual executable). This is the authoritative commitment: it's a
 *     full-program, collision-resistant digest, so a contract whose script
 *     hashes to this value IS the bundled program, byte for byte.
 *   - EXPECTED_NEF_CHECKSUM — the NEF's built-in 4-byte checksum. Only 32
 *     bits, so it is NOT collision-resistant; kept solely as a cheap
 *     pre-filter / fallback for RPC nodes that don't surface the script.
 *
 * Wired into `predev` and `prebuild` so Vite always sees up-to-date values.
 *
 * If the contract hasn't been compiled yet, this script does NOT overwrite an
 * existing committed `nef-checksum.ts` — it leaves the last-committed values
 * in place so the UI can build in a CI environment with no Java/Gradle
 * toolchain (e.g. Cloudflare Pages). Empty/zero placeholders are only written
 * on a first-time scaffold where no committed file exists at all.
 */

import { readFileSync, writeFileSync, existsSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { sc, u } from '@cityofzion/neon-js';

const here = dirname(fileURLToPath(import.meta.url));
const nefPath = resolve(here, '..', '..', 'contract', 'build', 'neow3j', 'VestingVault.nef');
const outPath = resolve(here, '..', 'src', 'lib', 'nef-checksum.ts');

let checksum = 0;
let scriptSha256 = '';
let note = 'NEF not yet compiled — run `./gradlew :contract:neow3jCompile` then re-run `npm run sync-checksum`.';

if (existsSync(nefPath)) {
  const buf = readFileSync(nefPath);
  // NEF format: last 4 bytes are the checksum, little-endian uint32.
  checksum = buf.readUInt32LE(buf.length - 4);
  // Parse the NEF and hash the script section — the deployed contract's
  // script (fetched via RPC) must hash to exactly this.
  const nef = sc.NEF.fromBuffer(buf);
  scriptSha256 = u.sha256(nef.script);
  note = `Derived from ${nefPath}. Re-runs automatically before every \`npm run dev\` and \`npm run build\`.`;
} else {
  console.warn(`[sync-checksum] ${nefPath} not found (no contract toolchain in this environment?).`);
  if (existsSync(outPath)) {
    // Keep committed values — do not clobber a valid checksum/hash with zeros.
    console.warn(`[sync-checksum] Keeping existing committed ${outPath}.`);
    process.exit(0);
  }
  console.warn('[sync-checksum] No committed file either — writing empty placeholder.');
  console.warn('[sync-checksum] Run `./gradlew :contract:neow3jCompile` to enable verification.');
}

const generated = `// AUTO-GENERATED by scripts/sync-checksum.mjs — do not edit by hand.
// ${note}

/**
 * SHA-256 of the \`VestingVault.nef\` script bytes — the authoritative
 * commitment the UI uses to decide the "Verified" badge. The deployed
 * contract's script (fetched via RPC) is hashed the same way and compared
 * byte-for-byte equivalent. Empty string means the NEF wasn't compiled at
 * build time, in which case verification falls back to the checksum. See
 * lib/verification.ts.
 */
export const EXPECTED_NEF_SCRIPT_SHA256: string = '${scriptSha256}';

/**
 * Last 4 bytes of \`VestingVault.nef\` (little-endian uint32). Only a 32-bit
 * value and therefore NOT collision-resistant — used as a fast pre-filter
 * and as a fallback when an RPC node omits the script from getcontractstate.
 * Never the sole basis for "Verified".
 */
export const EXPECTED_NEF_CHECKSUM = ${checksum};
`;

mkdirSync(dirname(outPath), { recursive: true });

// Idempotency: skip the write when nothing changed, so we don't trigger
// unnecessary HMR cycles every time `vite dev` starts.
const existing = existsSync(outPath) ? readFileSync(outPath, 'utf8') : '';
if (existing === generated) {
  console.log(`[sync-checksum] up-to-date (checksum=${checksum})`);
} else {
  writeFileSync(outPath, generated, 'utf8');
  console.log(`[sync-checksum] wrote ${outPath} (checksum=${checksum})`);
}
